// ── 模板库 API ─────────────────────────────────────────────────────────────

export class TemplateLibrary {
  /** 列出所有模板 */
  static listTemplates(): TemplateMetadata[] {
    return Object.values(TEMPLATES);
  }

  /** 获取指定模板 */
  static getTemplate(id: TemplateId): TemplateMetadata {
    const template = TEMPLATES[id];
    if (!template) {
      throw new ForgeError(
        'FORGE_TEMPLATE_NOT_FOUND',
        `Unknown template ID: ${id}`,
        { retryable: false },
      );
    }
    return template;
  }

  /** 根据语言/传输方式推荐模板 */
  static recommendTemplate(
    language?: 'python' | 'typescript',
    transport?: 'stdio' | 'streamable-http',
  ): TemplateId {
    if (transport === 'streamable-http') return 'http-api-wrapper';
    if (language === 'python') return 'python-fastmcp-stdio';
    return 'nodejs-stdio';
  }

  /**
   * 验证占位符值，返回填充后的值映射。
   * 必填占位符缺失时抛出 FORGE_INVALID_REQUEST。
   */
  static resolvePlaceholders(
    templateId: TemplateId,
    values: Record<string, string>,
  ): Record<string, string> {
    const template = TemplateLibrary.getTemplate(templateId);
    const resolved: Record<string, string> = {};

    for (const ph of template.placeholders) {
      const value = values[ph.name] ?? ph.defaultValue;

      if (ph.required && !value) {
        throw new ForgeError(
          'FORGE_INVALID_REQUEST',
          `Missing required placeholder: ${ph.name}`,
          { detail: `Placeholder '${ph.name}' is required for template '${templateId}'`, retryable: false },
        );
      }

      if (!value) continue;

      // Pattern validation
      if (ph.pattern) {
        const regex = new RegExp(ph.pattern);
        if (!regex.test(value)) {
          throw new ForgeError(
            'FORGE_INVALID_REQUEST',
            `Invalid value for placeholder ${ph.name}: '${value}' does not match pattern ${ph.pattern}`,
            { retryable: false },
          );
        }
      }

      // maxLength validation
      if (ph.maxLength && value.length > ph.maxLength) {
        throw new ForgeError(
          'FORGE_INVALID_REQUEST',
          `Placeholder ${ph.name} value exceeds maxLength ${ph.maxLength}`,
          { retryable: false },
        );
      }

      resolved[ph.name] = value;
    }

    return resolved;
  }

  /**
   * 将模板文件中的占位符替换为实际值，返回填充后的文件列表。
   */
  static renderTemplate(
    templateId: TemplateId,
    values: Record<string, string>,
  ): { files: Array<{ path: string; content: string }>; entryPoint: string } {
    const template = TemplateLibrary.getTemplate(templateId);
    const resolved = TemplateLibrary.resolvePlaceholders(templateId, values);

    const renderedFiles = template.files.map((file) => {
      let content = file.content;
      for (const [key, val] of Object.entries(resolved)) {
        content = content.replaceAll(`{{${key}}}`, val);
      }
      // Also replace in file path
      let filePath = file.path;
      for (const [key, val] of Object.entries(resolved)) {
        filePath = filePath.replaceAll(`{{${key}}}`, val);
      }
      return { path: filePath, content };
    });

    return { files: renderedFiles, entryPoint: template.entryPoint };
  }
}
