const HTTP_INDEX_TS = `/**
 * {{SERVER_NAME}} — MCP Server (HTTP API Wrapper, streamable-http)
 *
 * Generated by Lingxiao MCP Forge.
 * Author: {{AUTHOR}}
 */
import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { StreamableHTTPServerTransport } from "@modelcontextprotocol/sdk/server/streamableHttp.js";
import { z } from "zod";
import http from "http";

const PORT = parseInt(process.env.PORT || "{{PORT}}", 10);
const UPSTREAM_API_URL = process.env.UPSTREAM_API_URL || "{{UPSTREAM_API_URL}}";
const UPSTREAM_API_KEY = process.env.UPSTREAM_API_KEY || "";

const server = new McpServer({
  name: "{{SERVER_NAME}}",
  version: "1.0.0",
});

// Example tool: proxy a request to the upstream API
server.tool(
  "fetch_upstream",
  "Fetch data from the upstream API",
  { path: z.string().describe("API path to fetch") },
  async ({ path }) => {
    try {
      const url = \`\${UPSTREAM_API_URL}\${path}\`;
      const headers: Record<string, string> = {};
      if (UPSTREAM_API_KEY) {
        headers["Authorization"] = \`Bearer \${UPSTREAM_API_KEY}\`;
      }
      const resp = await fetch(url, { headers });
      const data = await resp.text();
      return {
        content: [{ type: "text" as const, text: data }],
        isError: !resp.ok,
      };
    } catch (err) {
      return {
        content: [{ type: "text" as const, text: String(err) }],
        isError: true,
      };
    }
  },
);

// Start streamable-http transport
const httpServer = http.createServer(async (req, res) => {
  if (req.url === "/mcp") {
    const transport = new StreamableHTTPServerTransport({ sessionIdGenerator: undefined });
    await server.connect(transport);
    await transport.handleRequest(req, res);
  } else {
    res.writeHead(404).end("Not found");
  }
});

httpServer.listen(PORT, () => {
  console.log("{{SERVER_NAME}} MCP server listening on port " + PORT);
});
`;

