/** 全部字形变体，运行时随机择一。索引 0 为默认（宋骨）。 */
export const GLYPH_VARIANTS: readonly GlyphVariant[] = [GLYPH_SONG, GLYPH_HEI, GLYPH_KAI];

/** 默认变体（宋骨）。 */
export const DEFAULT_GLYPH: GlyphVariant = GLYPH_VARIANTS[0];

/** 按 [0,1) 种子确定性选一套变体。 */
export function pickGlyph(seed: number): GlyphVariant {
  const n = GLYPH_VARIANTS.length;
  const i = Math.floor(seed * n) % n;
  return GLYPH_VARIANTS[i < 0 ? 0 : i];
}

