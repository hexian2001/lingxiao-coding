export const GLYPH_HEIGHT = DEFAULT_GLYPH.height;

/** 默认变体几何中心（子像素坐标）。 */
export const GLYPH_CENTER = DEFAULT_GLYPH.center;

/** cell 含任意块像素即为「亮」。 */
export function isLitCell(ch: string): boolean {
  return ch === '\u2588' || ch === '\u2580' || ch === '\u2584';
}

/** cell 上子像素点亮。 */
export function topOn(ch: string): boolean {
  return ch === '\u2588' || ch === '\u2580';
}

/** cell 下子像素点亮。 */
export function bottomOn(ch: string): boolean {
  return ch === '\u2588' || ch === '\u2584';
}

